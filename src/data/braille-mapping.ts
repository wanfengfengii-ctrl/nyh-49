function createDots(activePositions: [number, number][]): boolean[][] {
  const dots: boolean[][] = [];
  for (let r = 0; r < CELL_ROWS; r++) {
    dots.push([]);
    for (let c = 0; c < CELL_COLS; c++) {
      dots[r].push(false);
    }
  }
  for (const [r, c] of activePositions) {
    if (r >= 0 && r < CELL_ROWS && c >= 0 && c < CELL_COLS) {
      dots[r][c] = true;
    }
  }
  return dots;
}

